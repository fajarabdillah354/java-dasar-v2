public class OperasiMath {
//    dalam bahasa java kita dapat melakukan beberapa operasi math seperti tambah,bagi,kurang,dan kali

    public static void main(String[] args) {
        var a = 10;
        var b = 20;
        /*
        Operasi Math
         */
        System.out.println(a+b);//operasi  penjumlahan
        System.out.println(b/a);//operasi pambagian
        System.out.println(a*b);//operasi perkalian
        System.out.println(a-b);//operasi pengurangan
    }


}
